const normalizeText = (value = '') =>
  String(value)
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .trim();

const THEME_KEYWORDS = [
  ['cacamba-residuos', ['cacamba', 'caçamba', 'residuo', 'resíduo', 'entulho', 'descarte']],
  ['marcenaria', ['marcenaria', 'moveis planejados', 'moveis sob medida', 'ferragens', 'mdf', 'madeira']],
  ['automacao-tecnologia', ['automacao', 'automação', 'tecnologia', 'smart home', 'bim', 'ia', 'inteligente']],
  ['consultoria-planejamento', ['briefing', 'consultoria', 'planejamento', 'evf', 'viabilidade', 'cronograma']],
  ['projetos-engenharia', ['engenharia', 'projeto executivo', 'projeto arquitetonico', 'projeto arquitetônico', 'iccri', 'custo', 'reforma', 'obra']],
  ['arquitetura', ['arquitetura', 'interiores', 'design', 'decoracao', 'decoração', 'estilo']],
];

export const EDITORIAL_THEME_OPTIONS = Object.values(EDITORIAL_THEME_REGISTRY)
  .filter((theme) => theme.id !== 'transversal')
  .map((theme) => ({ id: theme.id, label: theme.label }));

export const resolveEditorialThemeId = (article = {}) => {
  const explicit = normalizeText(article.editorialThemeId || article.editorialTheme || article.theme);
  if (explicit && EDITORIAL_THEME_REGISTRY[explicit]) return explicit;

  const category = normalizeText(article.category);
  if (category.includes('marcen')) return 'marcenaria';
  if (category.includes('engenhar') || category.includes('reforma') || category.includes('obra')) return 'projetos-engenharia';
  if (category.includes('arquitet')) return 'arquitetura';

  const haystack = normalizeText([
    article.slug,
    article.title,
    article.subtitle,
    article.excerpt,
    article.summary,
    article.category,
    ...(Array.isArray(article.tags) ? article.tags : []),
  ].filter(Boolean).join(' '));

  for (const [themeId, keywords] of THEME_KEYWORDS) {
    if (keywords.some((keyword) => haystack.includes(normalizeText(keyword)))) {
      return themeId;
    }
  }

  return 'transversal';
};

export const getEditorialTheme = (article = {}) =>
  EDITORIAL_THEME_REGISTRY[resolveEditorialThemeId(article)] || EDITORIAL_THEME_REGISTRY.transversal;

